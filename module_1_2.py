example  = input("Введите любую строку из несольких символов: ")
print (example[0], "- это первый символ строки")
print (example[-1], "- это последний символ строки ")
print (example[len(example)//2:], "- это вторая половина строки ")
print (example[::-1], "- это строка наоборот")
print (example[1::2], "- это каждый второй символ строки")